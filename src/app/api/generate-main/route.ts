    fromArray.length > 0 ? fromArray : legacySingle.length > 0 ? legacySingle : [];

  const bananaRaw =
    typeof body.bananaImageModel === "string"
      ? body.bananaImageModel.trim()
      : typeof body.step1ImageModel === "string"
